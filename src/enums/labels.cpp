#include "labels.h"

std::string GetLabel(int16_t id) {
    if (id > SLOT_EMPTY) {
        if (id < BLOCK_MAX) {
            return blockLabels[id];
        }
        if (id >= ITEM_SHOVEL_IRON) {
            return itemLabels[id-ITEM_SHOVEL_IRON];
        }
    }
    return "Invalid";
}

